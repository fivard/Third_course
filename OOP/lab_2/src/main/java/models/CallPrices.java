package models;

public class CallPrices {
    private Integer inNetwork;
    private Integer outNetwork;
    private Integer stationary;

    public Integer getInNetwork() {
        return inNetwork;
    }

    public void setInNetwork(Integer inNetwork) {
        this.inNetwork = inNetwork;
    }

    public Integer getOutNetwork() {
        return outNetwork;
    }

    public void setOutNetwork(Integer outNetwork) {
        this.outNetwork = outNetwork;
    }

    public Integer getStationary() {
        return stationary;
    }

    public void setStationary(Integer stationary) {
        this.stationary = stationary;
    }

}
